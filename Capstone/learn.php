        <li>Dengue is a viral infection transmitted to humans through the bite of infected mosquitoes. The primary vectors that transmit the disease are Aedes aegypti mosquitoes and, to a lesser extent, Ae. albopictus.</li>

        <li>The virus responsible for causing dengue, is called dengue virus (DENV). There are four DENV serotypes and it is possible to be infected four times.</li>

        <li>Severe dengue is a leading cause of serious illness and death in some Asian and Latin American countries. It requires management by medical professionals.</li>

        <li>There is no specific treatment for dengue/severe dengue. Early detection of disease progression associated with severe dengue, and access to proper medical care lowers fatality rates of severe dengue to below 1%.</li>

        <li>Dengue is found in tropical and sub-tropical climates worldwide, mostly in urban and semi-urban areas.</li>

        <li>The global incidence of dengue has grown dramatically with about half of the world's population now at risk. Although an estimated 100-400 million infections occur each year, over 80% are generally mild and asymptomatic.</li>

        <li>Dengue prevention and control depends on effective vector control measures. Sustained community involvement can improve vector control efforts substantially.</li>

        <li>While many DENV infections produce only mild illness, DENV can cause an acute flu-like illness. Occasionally this develops into a potentially lethal complication, called severe dengue.</li>

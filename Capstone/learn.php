    <strong><p>Overview</p></strong>

        <p>Dengue is a mosquito-borne viral disease that has rapidly spread to all regions of WHO in recent years. Dengue virus is transmitted by female mosquitoes mainly of the species Aedes aegypti and, to a lesser extent, Ae. albopictus. These mosquitoes are also vectors of chikungunya, yellow fever and Zika viruses. Dengue is widespread throughout the tropics, with local variations in risk influenced by climate parameters as well as social and environmental factors.<br><br><br>

        Dengue causes a wide spectrum of disease. This can range from subclinical disease (people may not know they are even infected) to severe flu-like symptoms in those infected. Although less common, some people develop severe dengue, which can be any number of complications associated with severe bleeding, organ impairment and/or plasma leakage. Severe dengue has a higher risk of death when not managed appropriately. Severe dengue was first recognized in the 1950s during dengue epidemics in the Philippines and Thailand. Today, severe dengue affects most Asian and Latin American countries and has become a leading cause of hospitalization and death among children and adults in these regions.<br><br><br>

        Dengue is caused by a virus of the Flaviviridae family and there are four distinct, but closely related, serotypes of the virus that cause dengue (DENV-1, DENV-2, DENV-3 and DENV-4). Recovery from infection is believed to provide lifelong immunity against that serotype. However, cross-immunity to the other serotypes after recovery is only partial, and temporary. Subsequent infections (secondary infection) by other serotypes increase the risk of developing severe dengue.<br><br> 
